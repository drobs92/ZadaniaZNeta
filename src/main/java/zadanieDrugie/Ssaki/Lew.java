package zadanieDrugie.Ssaki;

public class Lew {
    private double waga;
    private double predkosc;

    public Lew() {

    }

    public Lew(double waga, double predkosc) {
        this.waga = waga;
        this.predkosc = predkosc;

    }

    public double getWaga() {
        return waga;
    }

    public void setWaga(double waga) {
        this.waga = waga;
    }

    public double getPredkosc() {
        return predkosc;
    }

    public void setPredkosc(double predkosc) {
        this.predkosc = predkosc;
    }

}
